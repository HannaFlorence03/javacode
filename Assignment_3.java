package assign_part1;

public class Assignment_3 {
	
	// Static method for addition
	    public static int addition(int a, int b) {
	        return a + b;
	    }
	 // Static method for Subtraction 
	    public static int Subraction(int a, int b) {
	        return a - b;
	    }
	 // Static method for multiplication
	    public static int multiplication(int a, int b) {
	        return a * b;
	    }
	 // Static method for division
	    public static int division(int a, int b) {
	        return a / b;
	    
	}
	
public static void main(String[] args) {
		
	System.out.println(addition(10,30));
	System.out.println(Subraction(30,20));
	System.out.println(multiplication(5,20));
	System.out.println(division(25,50));
			
	}
}
